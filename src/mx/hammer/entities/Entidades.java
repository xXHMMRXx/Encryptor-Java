
package mx.hammer.entities;


public class Entidades 
{

    private String cMensaje, dMensaje, Combo, RutaDevice, RutaArchivos, RutaCarpetas;
    private int switchh;
    
    public Entidades() {}

    public String getcMensaje() {
        return cMensaje;
    }

    public void setcMensaje(String cMensaje) {
        this.cMensaje = cMensaje;
    }

    public String getdMensaje() {
        return dMensaje;
    }

    public void setdMensaje(String dMensaje) {
        this.dMensaje = dMensaje;
    }

    public String getCombo() {
        return Combo;
    }

    public void setCombo(String Combo) {
        this.Combo = Combo;
    }

    public String getRutaDevice() {
        return RutaDevice;
    }

    public void setRutaDevice(String RutaDevice) {
        this.RutaDevice = RutaDevice;
    }

    public String getRutaArchivos() {
        return RutaArchivos;
    }

    public void setRutaArchivos(String RutaArchivos) {
        this.RutaArchivos = RutaArchivos;
    }

    public String getRutaCarpetas() {
        return RutaCarpetas;
    }

    public void setRutaCarpetas(String RutaCarpetas) {
        this.RutaCarpetas = RutaCarpetas;
    }

    public int getSwitchh() {
        return switchh;
    }

    public void setSwitchh(int switchh) {
        this.switchh = switchh;
    }
    

}
